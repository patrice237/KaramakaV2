package Projet;

/**
 * Énumération des couleurs disponibles.
 * Utilisée pour représenter les différentes couleurs d'éléments dans le jeu.
 */
public enum Couleur {
    
    /**
     * Représente la couleur bleue.
     */
    bleu,

    /**
     * Représente la couleur verte.
     */
    vert,

    /**
     * Représente la couleur rouge.
     */
    rouge,

    /**
     * Représente une couleur multicolore ou une combinaison de couleurs.
     */
    multi_color,
}
