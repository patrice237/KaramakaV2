package Projet;

/**
 * Énumération représentant les différents niveaux karmiques dans le jeu Karmaka.
 * Chaque niveau symbolise une étape dans le parcours spirituel du joueur, allant du plus bas au plus élevé.
 * Les niveaux sont utilisés pour suivre les progrès du joueur tout au long du jeu.
 *
 * Les niveaux karmiques sont les suivants :
 * - Bousier_1 : Le niveau le plus bas, symbolisant le début du parcours karmique.
 * - Serpent_2 : Le deuxième niveau, représentant une évolution dans le parcours.
 * - Loup_3 : Un niveau intermédiaire indiquant une progression significative.
 * - Gorille_4 : L'avant-dernier niveau, près de l'atteinte de la transcendance.
 * - Transcendance_5 : Le niveau le plus élevé, marquant l'atteinte de l'illumination karmique.
 */
public enum NiveauKarmique {
    Bousier_1, Serpent_2, Loup_3, Gorille_4, Transcendance_5,
}